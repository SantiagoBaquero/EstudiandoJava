public class EjemploPrimitivosEnteros {
    public static void main(String[] args) {


        byte numeroByte =7;
        System.out.println("numeroByte = " + numeroByte);
        System.out.println("Tipo byte corresponde en byte a " + Byte.BYTES);
        System.out.println("Tipo byte corresponde en byte a " + Byte.SIZE);
        System.out.println("Valor maximo de un byte:" + Byte.MAX_VALUE);
        System.out.println("Valor minimo de un byte:" + Byte.MIN_VALUE);


        short numeroShort =3276;
        System.out.println("numeroShort = " + numeroShort);
        System.out.println("Tipo short corresponde en Short a " + Short.BYTES);
        System.out.println("Tipo short corresponde en Short a " + Short.SIZE);
        System.out.println("Valor maximo de un short:" + Short.MAX_VALUE);
        System.out.println("Valor minimo de un short:" + Short.MIN_VALUE);

        int numeroInt =2147483647;
        System.out.println("numeroInt = " + numeroInt);
        System.out.println("Tipo int corresponde en int a " + Integer.BYTES);
        System.out.println("Tipo int corresponde en int a " + Integer.SIZE);
        System.out.println("Valor maximo de un int:" + Integer.MAX_VALUE);
        System.out.println("Valor minimo de un int:" + Integer.MIN_VALUE);

        long numeroLong =9223372036854775807L;
        System.out.println("numeroLong = " + numeroInt);
        System.out.println("Tipo int corresponde en Long a " + Long.BYTES);
        System.out.println("Tipo int corresponde en Long a " + Long.SIZE);
        System.out.println("Valor maximo de un Long:" + Long.MAX_VALUE);
        System.out.println("Valor minimo de un Long:" + Long.MIN_VALUE);

        var numeroVar= 127;
        System.out.println("numeroVar = " + numeroVar);



    }
}
